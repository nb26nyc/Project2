package GroupProject2;

public class WL2 {
    public static void main(String[] args) {

        int k=20;
        while (k>=1){
            System.out.println(k+ " ");
            k -=2;
        }
        System.out.println();
        for (int number=20; number>=1; number -=2){
            System.out.println(number+ " ");
        }
    }
}
